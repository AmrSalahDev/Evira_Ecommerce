class EditProfileScreen extends StatefulWidget {
  const EditProfileScreen({super.key});

  @override
  State<EditProfileScreen> createState() => _EditProfileScreenState();
}

class _EditProfileScreenState extends State<EditProfileScreen>
    with StatefulScreenMixin {
  late final TextEditingController phoneNumberController;
  late final TextEditingController dateController;
  late final TextEditingController genderController;
  late final TextEditingController fullNameController;
  late final TextEditingController nickNameController;
  late final TextEditingController emailController;
  late final GlobalKey<FormState> formKey;
  String phoneCode = '';
  String profileImage = '';
  bool _controllersInitialized = false;

  @override
  void initState() {
    super.initState();
    phoneNumberController = TextEditingController();
    dateController = TextEditingController();
    genderController = TextEditingController();
    fullNameController = TextEditingController();
    nickNameController = TextEditingController();
    emailController = TextEditingController();
    formKey = GlobalKey<FormState>();

    context.read<TextFieldCubit>().setRequiredFields([
      'name',
      'email',
      'phone',
      'nickName',
      'gender',
      'date',
    ]);

    context.read<ProfileInfoBloc>().add(GetProfileInfoEvent());
  }

  @override
  void dispose() {
    phoneNumberController.dispose();
    dateController.dispose();
    genderController.dispose();
    fullNameController.dispose();
    nickNameController.dispose();
    emailController.dispose();
    super.dispose();
  }

  @override
  String get title => EviraLang.of(context).editProfile;

  @override
  Widget? buildBottomNavigationBar() {
    return Padding(
      padding: EdgeInsets.only(bottom: 30.h),
      child: BlocConsumer<EditProfileBloc, EditProfileState>(
        listener: (context, state) {
          if (state is UpdateProfileError) {
            getIt<ToastService>().showErrorToast(
              context: context,
              message: state.message,
            );
          }
        },
        builder: (context, state) {
          final isLoading = state is UpdateProfileLoading;
          return BlocBuilder<TextFieldCubit, TextFieldState>(
            buildWhen: (previous, current) => previous != current,
            builder: (context, state) {
              final isEnabled =
                  state is TextFieldsUpdated && state.allFieldsFilled;
              return CustomButton(
                isLoading: isLoading,
                handleNoInternet: true,
                title: EviraLang.of(context).update,
                onPressed: isEnabled
                    ? () {
                        updateProfile();
                      }
                    : null,
                backgroundColor: isEnabled
                    ? context.buttonActiveColor
                    : context.buttonInactiveColor,
                textColor: isEnabled
                    ? context.textActiveColor
                    : context.textInactiveColor,
              );
            },
          );
        },
      ),
    );
  }

  void updateProfile() {
    if (formKey.currentState?.validate() ?? false) {
      final profileEntity = ProfileEntity(
        fullname: fullNameController.text.trim(),
        nickname: nickNameController.text.trim(),
        anotherEmail: emailController.text.trim(),
        phone: phoneCode + phoneNumberController.text.trim(),
        gender: genderController.text.trim(),
        dateOfBirth: dateController.text.trim(),
        profileImage: profileImage,
      );
      context.read<EditProfileBloc>().add(
        UpdateProfileEvent(profile: profileEntity),
      );
    }
  }

  @override
  Widget buildBody(BuildContext context) {
    return SingleChildScrollView(
      clipBehavior: Clip.none,
      child: Form(
        key: formKey,
        child: BlocConsumer<ProfileInfoBloc, ProfileInfoState>(
          listener: (context, state) {
            if (state is ProfileInfoLoaded && !_controllersInitialized) {
              // Use WidgetsBinding.instance.addPostFrameCallback to avoid setState during build
              WidgetsBinding.instance.addPostFrameCallback((_) {
                if (mounted) {
                  // Set the initial values on controllers when profile is loaded
                  fullNameController.text = state.profile.fullname;
                  nickNameController.text = state.profile.nickname;
                  emailController.text = state.profile.anotherEmail;
                  dateController.text = state.profile.dateOfBirth;
                  genderController.text = state.profile.gender;
                  phoneNumberController.text = state.profile.phone;
                  profileImage = state.profile.profileImage;

                  final textFieldCubit = context.read<TextFieldCubit>();
                  textFieldCubit.updateField('name', state.profile.fullname);
                  textFieldCubit.updateField(
                    'nickName',
                    state.profile.nickname,
                  );
                  textFieldCubit.updateField(
                    'email',
                    state.profile.anotherEmail,
                  );
                  textFieldCubit.updateField('date', state.profile.dateOfBirth);
                  textFieldCubit.updateField('gender', state.profile.gender);
                  textFieldCubit.updateField('phone', state.profile.phone);

                  _controllersInitialized = true;
                }
              });
            }
          },
          builder: (context, state) {
            return Column(
              children: [
                SizedBox(height: 30.h),
                CustomTextField(
                  controller: fullNameController,
                  hintText: EviraLang.of(context).fullName,
                  keyboardType: TextInputType.name,
                  fieldKey: 'name',
                  validator: (value) => Validators.name(value: value),
                ),
                SizedBox(height: 25.h),
                CustomTextField(
                  controller: nickNameController,
                  hintText: EviraLang.of(context).nickName,
                  fieldKey: 'nickName',
                  keyboardType: TextInputType.name,
                  validator: (value) => Validators.name(value: value),
                ),
                SizedBox(height: 25.h),
                DateTimeTextFieldPart(dateController: dateController),
                SizedBox(height: 25.h),
                CustomTextField(
                  fieldKey: 'email',
                  controller: emailController,
                  keyboardType: TextInputType.emailAddress,
                  validator: (value) => Validators.checkDuplicateEmail(
                    firstEmail: value,
                    secondEmail:
                        Supabase.instance.client.auth.currentUser!.email,
                  ),
                  hintText: EviraLang.of(context).email,
                  suffixIcon: FontAwesomeIcons.envelope,
                ),
                SizedBox(height: 25.h),
                PhoneNumberTextFieldPart(
                  controller: phoneNumberController,
                  onCountrySelected: (phoneCode, countryCode, phoneNumber) {
                    this.phoneCode = '+$phoneCode';
                  },
                ),
                SizedBox(height: 25.h),
                GenderTextFieldPart(controller: genderController),
              ],
            );
          },
        ),
      ),
    );
  }
}
